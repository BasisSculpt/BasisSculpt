package basissculpt.etc;

import java.util.*;

/**
 * BasisSculpt - Sculpting Gaussian Basis Sets via Norm Control
 *
 * @author M. Macernis
 * @version 2025.4.v1
 */
public class Args {

    private String[] args;
    Map<String, String> mapArgs = new TreeMap<>();

    public Args(String[] args) {
        this.args = args;
        parse();
    }

    private void parse() {
        for (int i = 0; i < args.length; i++) {
            String arg = args[i];
            if (arg.startsWith("--")) {
                String key = arg.substring(2);
                String value = null;
                if (i + 1 < args.length && !args[i + 1].startsWith("--")) {
                    value = args[i + 1];
                    i++;
                }
                mapArgs.put(key, value);
            }
        }
    }

    public Map<String, String> getMapArgs() {
        return mapArgs;
    }
}
